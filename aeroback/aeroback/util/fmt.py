# Print human readable file size
def byte_size(num):
    for x in ['bytes', 'KB', 'MB', 'GB']:
        if num < 1000.0 and num > -1000.0:
                return "%3.1f %s" % (num, x)

        num /= 1000.0
    return "%3.1f %s" % (num, 'TB')

